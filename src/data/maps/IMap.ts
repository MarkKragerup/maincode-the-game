import { ETileTypes } from '../../components/tile/TileFactory';

/** Maps contain a map of tiles, a spawn point and a track id for the music. */
export type IMap = {
	spawn: { x: number; y: number };
	track?: string;
	board: ETileTypes[][];
};

/** Pads a map with a number of walls in all directions. */
export const padMap = (map: IMap): IMap => {
	const mapXDimension = map.board?.[0]?.length;

	const wallsX = Array.from(Array(mapXDimension), () => ETileTypes.wall);

	return {
		...map,
		spawn: { x: map.spawn.x + 1, y: map.spawn.y + 1 },
		board: [[...wallsX], ...map.board.map((col) => [ETileTypes.wall, ...col, ETileTypes.wall]), [...wallsX]]
	};
};

